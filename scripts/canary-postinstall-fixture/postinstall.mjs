#!/usr/bin/env node

import { formatReport, runProbe } from './probe.mjs';

// #185: lifecycle-скрипт (postinstall) фикстуры-«зависимости». npm зовёт его при установке
// пакета — то есть в тот же момент, что и postinstall настоящей скомпрометированной
// зависимости при `npm ci` в гейте, с тем же унаследованным окружением. Скрипт применяет
// канареечный подход (probe.mjs) и печатает воспроизводимый отчёт, какие каналы к секретам
// открыты изнутри lifecycle-скрипта.
//
// ВСЕГДА exit 0 — критерий готовности «фикстура не ломает обычный `npm ci` на честном
// коде»: это ИЗМЕРЕНИЕ канала, а не красный чек (как и сама канарейка на фазе 3). Любая
// ошибка скана глотается и тоже даёт exit 0, иначе битый файл/права уронили бы `npm ci`.
try {
    console.log(formatReport(runProbe()));
} catch (e) {
    console.log(`Postinstall-канарейка: скан не выполнен (${e?.code ?? e?.message ?? 'ошибка'})`);
}
process.exit(0);
