/**
 * Ход игрока в записи боя. Бот полностью детерминирован seed'ом,
 * поэтому его действия в реплей не записываются — только действия игрока.
 */
export type TReplayMove =
    /** Перемещение танка по горизонтали (пиксели, знак — направление). */
    | { kind: 'move'; delta: number }
    /**
     * Выстрел. Угол — точный float64: физика чувствительна к младшим битам,
     * квантование сломало бы идентичность воспроизведения. Сила — целое 1–20.
     */
    | { kind: 'fire'; angle: number; power: number };

/**
 * Полная запись боя: seed + логический размер поля + ходы игрока.
 * Тип seed значим: `createSeededRandom` использует число напрямую,
 * а строку хеширует — `123` и `'123'` дают разные последовательности.
 *
 * `width`/`height` — логический (CSS) размер поля в пикселях на момент боя.
 * Вся физика идёт в абсолютных пикселях (рельеф `Ground.generate` тратит по
 * одному `random()` на пиксель ширины, танки стоят в `W/4` и `3W/4`, скорость
 * снаряда завязана на размер), поэтому воспроизведение обязано идти на том же
 * размере — иначе рельеф, ветер и траектории разойдутся, а счёт не совпадёт.
 * Реплей проигрывается на canvas этого фиксированного размера, вписанном в
 * экран CSS-масштабированием.
 */
export type TReplay = {
    seed: number | string;
    width: number;
    height: number;
    moves: TReplayMove[];
};
